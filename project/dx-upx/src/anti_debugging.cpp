struct _ANTI_HASHSIGN {
    unsigned char CURFUN2INIT[4];
    unsigned char CURFUN2ANTI[4];
} ANTI_HASHSIGN={
    {0x10, 0xAB, 0xAB, 0xAB},
    {0x11, 0xAB, 0xAB, 0xAB}
};

bool anti_debugging::update_anti_debugging(uint8_t* shellcode,
        size_t length,
        long rel_curfun2init,
        long rel_curfun2antifun,
        arch_bit mode) 
{
    off_t offx = 0;
    
    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)ANTI_HASHSIGN.CURFUN2INIT, sizeof(ANTI_HASHSIGN.CURFUN2INIT));
    //LOGD("offx : %d", offx);
    if (offx <0 ) {
        LOGE("can not find cur2checkintegeriry...");
        return false;
    }
    //LOGD("relCurfun2IntegrityCheck:%lx", relCurfun2IntegrityCheck);
    update_magic(shellcode+offx, rel_curfun2init, mode);
    // *(unsigned int *)(shellcode + offx) = (unsigned int)rel_curfun2init;

    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)ANTI_HASHSIGN.CURFUN2ANTI, sizeof(ANTI_HASHSIGN.CURFUN2ANTI));
    //LOGD("offx : %d", offx);
    if (offx <0 ) {
        LOGE("can not find curfun2antifun...");
        return false;
    }
    //LOGD("relCurfun2IntegrityCheck:%lx", relCurfun2IntegrityCheck);
    update_magic(shellcode+offx, rel_curfun2antifun, mode);
    // *(unsigned int *)(shellcode + offx) = (unsigned int)rel_curfun2antifun;

    return true;
}


void anti_debugging::update_magic(uint8_t* magic, long relvalue, arch_bit mode){
    if(mode == ARCH_BIT_32){
        *(uint32_t*)(magic) = (uint32_t)relvalue;
    }else if(mode == ARCH_BIT_64){
        *(uint64_t *)(magic) = relvalue;
    }
}

