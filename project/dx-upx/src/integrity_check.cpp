struct _INTE_HASHSIGN {
    unsigned char CURFUN2INIT[4];
    unsigned char CURFUN2SYSCALL[4];
    unsigned char CURFUN2INTEGRITYCHECK[4];
    unsigned char HASHVAVL[4];
    unsigned char HASHCODEINDEX[4];
    unsigned char SONAMELEN[4];
    unsigned char SONAME[4];
} INTE_HASHSIGN={
    {0x10, 0xAB, 0xAB, 0xAB},
    {0x11, 0xAB, 0xAB, 0xAB},
    {0x12, 0xAB, 0xAB, 0xAB},
    {0x2b, 0x08, 0x2e, 0x5f},
    {0x3b, 0x08, 0x2e, 0x5f},
    {0x3c, 0x08, 0x2e, 0x5f},
    {0x3d, 0x08, 0x2e, 0x5f},
};

bool integrity_check::update_integrity_check(
    uint8_t* shellcode, 
    int length, 
    long relCurfun2Syscall, 
    long relCurfun2IntegrityCheck, 
    long relCurfun2init,
    std::string filename,
    arch_bit mode) {

    off_t offx = 0;
    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)INTE_HASHSIGN.CURFUN2INIT, 4);
    if(offx < 0) {
        LOGE("can not find cur2init hash...");
        return false;
    }
    // *(unsigned int *)(shellcode + offx) = (unsigned int)relCurfun2init;
    update_magic(shellcode+offx, relCurfun2init, mode);


    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)INTE_HASHSIGN.CURFUN2INTEGRITYCHECK, 4);
    //LOGD("offx : %d", offx);
    if (offx <0 ) {
        LOGE("can not find cur2checkintegeriry...");
        return false;
    }
    //LOGD("relCurfun2IntegrityCheck:%lx", relCurfun2IntegrityCheck);
    *(unsigned int *)(shellcode + offx) = (unsigned int)relCurfun2IntegrityCheck;
    // update_magic(shellcode+offx, relCurfun2IntegrityCheck, mode);

    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)INTE_HASHSIGN.CURFUN2SYSCALL, 4);
    //LOGD("offx : %d", offx);
    if(offx < 0 ){
        LOGE("can not find cur2checkintegeriry...");
        return false;
    }

    *(unsigned int *)(shellcode + offx) = (unsigned int)relCurfun2Syscall;;
    
    offx = 0;
    //put so name
    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)INTE_HASHSIGN.SONAME, 4);
    if (offx < 0) {
        LOGE("can not find so name...");
        return false;
    }

    size_t lastch = filename.find_last_of('/');
    if(lastch == std::string::npos){
        lastch = filename.find_last_of('\\');
    }

    const char* soname = filename.c_str()+lastch+1;// "libnative-lib.so";

    for(unsigned int i=0; i<strlen(soname)+1; i++){
        *(char*)(shellcode + offx+i) = soname[i];
    }

    //put the length of so name
    offx = elf_utils::search_bytes((const char*)shellcode, length, (const char*)INTE_HASHSIGN.SONAMELEN, 4);
    if(offx <= 0){
        LOGE("can not find so name length...");
        return false;
    }
    *(unsigned int *)(shellcode + offx) = strlen(soname)+1;
    
    return true;

}

bool integrity_check::update_hashcode(std::string filename){
    elf_utils elf(filename);
    unsigned char* buffer = elf.get_buffer();
    size_t file_length = elf.get_file_size();

    if(buffer == NULL || file_length <= 0){
        return false;
    }
    
    unsigned int hashcode = integrity_check::cal_hashcode(buffer, file_length);

    *(unsigned int*)&buffer[file_length-4] = hashcode;

    elf.update_file();

    return true;
}

void integrity_check::update_magic(uint8_t* magic, long relvalue, arch_bit mode){
    if(mode == ARCH_BIT_32){
        *(uint32_t*)(magic) = (uint32_t)relvalue;
    }else if(mode == ARCH_BIT_64){
        *(uint64_t *)(magic) = relvalue;
    }
}


unsigned int integrity_check::cal_hashcode(unsigned char* buf, size_t length){
    unsigned int b = 378551;
    unsigned int a = 63689;
    unsigned int hash = 0;

    for(size_t i=0; i<length-4; i++){
        hash = hash * a + buf[i];
        a *= b;
    }
    
    return (hash & 0x7FFFFFFF);
}


